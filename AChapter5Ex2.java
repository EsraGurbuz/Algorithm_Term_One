public class AChapter5Ex2{

	public static void main(String[] args){
		displayName();///
		displayAge(45);///
	}

	/**
	displayName is a void type of methods prints the name of the user
	*/

	public static void displayName() {
		System.out.println("John Dane");
	}

	/**
	displayAge is the void type of method that prints the age of the user
	@param1 integer type of age
	*/
	public static void displayAge(int age) {
		System.out.println(age);
	}

}
