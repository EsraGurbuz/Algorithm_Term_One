public class Pet1{

	private String name;
	private int age;
	private String location;

	public void setName(String n){
		name = n;
	}

	public void setAge(int a){
		age = a;
	}

	public void setLocation(String l){
		location = l;
	}

	public String getName(){
		return name;
	}

	public int getAge(){
		return age;
	}

	public String getLocation(){
		return location;
	}

}