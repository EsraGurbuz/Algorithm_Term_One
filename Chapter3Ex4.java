public class Chapter3Ex4{
	public static void main(String[] args){

		int value = 2;

		switch (value) {
			case 1:
			System.out.println("It is 1");
			break;

			case 2:
			System.out.println("It is 2");
			break;

			case 3:
			System.out.println("It is 3");
			break;

			default:
			System.out.println("Different value");
			break;
		}
	}
}
